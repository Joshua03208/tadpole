export const UK_PLACES: UkPlace[] = RAW.map((p) => ({ ...p, slug: slugifyPlace(p.name) }));

function normalize(s: string): string {
  return s
    .trim()
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "");
}

/** On-device search: prefix matches first, then substring, capped at `limit`. */
export function searchPlaces(query: string, limit = 8): UkPlace[] {
  const q = normalize(query);
  if (!q) return [];
  const starts: UkPlace[] = [];
  const contains: UkPlace[] = [];
  for (const place of UK_PLACES) {
    const n = normalize(place.name);
    if (n.startsWith(q)) starts.push(place);
    else if (n.includes(q)) contains.push(place);
  }
  return [...starts, ...contains].slice(0, limit);
}

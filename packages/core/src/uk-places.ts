// Bundled, on-device list of UK towns/cities for the coarse-area type-ahead.
// No network, no API key, no keystrokes leaving the device — this is the COARSE
// area shown to others (town/city level), never precise location.
//
// Each place's `slug` is derived deterministically from its canonical `name`, so
// selecting "Cardiff" always stores name "Cardiff" + slug "cardiff" (no
// fragmentation). The slug is the reconciliation key to the slugged `areas`
// table in the Activity Finder phase. Names are unique; coverage is the major
// population centres and is intentionally expandable.

export type UkPlace = { name: string; region: string; slug: string };

export function slugifyPlace(name: string): string {
  return name
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/['’.]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

